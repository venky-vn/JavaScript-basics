//while ,do while, for loops
 let x=1;
while(x<=5){
    console.log(x);
    x++;
}
console.log("loop ended");
//do while loop
do{
    console.log(x);
    x++;
}
while(x<=10);
console.log("loop ended");
//for loop
for(let i=1;i<=10;i++){
    console.log(i);
}
console.log("loop ended");
